a = 4
b = 6

# and ( & )
print("4 & 6 : ", a & b)
print("4 & 4 : ", a & a)
# or ( | )
print("4 | 6 : ", a | b)
print("4 | 4 : ", a | a)
# xor ( ^ )
print("4 ^ 6 : ", a ^ b)
print("4 ^ 4 : ", a ^ a)
# Negasi/kebalikan ( ~ )
print("~4 : ",  ~a)
print("~4 : ",  ~a)
# Left Shift ( << )
print("4 << 6 : ", a << b)
# Right Shift ( >> )
print("4 >> 6 : ", a >> b)
